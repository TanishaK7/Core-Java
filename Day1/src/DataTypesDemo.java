public class DataTypesDemo {
    public static void main(String[] args) {
        
        int val = 7+2 ; 
        int val2 = 100+2 ;
        float val3 = 637f+8.12f ;
        double val4 = 346d+56d ;
        double val5 = 225d+51.2d ;

        System.out.println("Value 1 = " + val);
        System.out.println("Value 2 = " + val2);
        System.out.println("Value 3 = " + val3);
        System.out.println("Value 4 = " + val4);
        System.out.println("Value 5 = " + val5);

        int total = 250;
        float percentage = total * 100 / 5;
        System.out.println("Percentage is = " + percentage);
    }
    
}
