public class IdentifiersDemo {
    public static void main(String[] args) {

        //valid identifiers
        int num = 34;
        float var = 56.07f;
        float no3 = 133f;
        double $num4 = 6547.77d;
        int _no5 = 89;

        System.out.println(num);
        System.out.println(var);
        System.out.println(no3);
        System.out.println($num4);
        System.out.println(_no5);

        //invalid indentifiers
       // int %no = 78;
       //  float 9var = 34.78f;
       // int num 5 = 45;


        
    }
    
}
